
using DevextremeAI.Communication;
using DevextremeAI.Communication.APIClient;
using DevextremeAI.Settings;

namespace DevextremeAIWebApp
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);


            // Add services to the container.

            builder.Services.AddHttpClient();
            builder.Services.AddSingleton<IAIEnvironment, AIEnvironment>();
            builder.Services.AddTransient<IOpenAIAPIClient, OpenAIAPIClient>();


            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}